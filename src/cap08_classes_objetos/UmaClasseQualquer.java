package cap08_classes_objetos;

/**
 * Uma classe qualquer, para exemplificar como criar uma classe no Java.
 *
 * Uma classe pode ter atributos e métodos. Vejam como defini-los no código abaixo.
 */
class UmaClasseQualquer {

    // atributos
    int atributoInt;
    String atributoString;

    // métodos
    void executaMetodoQualquer() {
        System.out.println("Imprimindo no console um texto qualquer.");
    }
}
